public class ArithAssn {
    public static void main(String[] args) {
        int a = 5;
        a += 5; // a = a + 5
        int b = 7;
        b -= 2;
        int c = 2;
        c *= 5; // 10

        int d = 8;
        d /= 2;
        System.out.println(a); // 10
        System.out.println(b); // 5
        System.out.println(c); // 10
        System.out.println(d); // 4

    }
}
